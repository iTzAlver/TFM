def news_calc(r):
    size = len(r)
    bp = []
    news = []
    for i in range(0, size):
        b0 = []
        if i not in bp:
            for j in range(0, size):
                if r[i][j] == 1:
                    b0.append(j)
                    if j not in bp:
                        bp.append(j)
            news.append(b0)
    return news


